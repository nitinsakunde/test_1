public class basic_assert {
	/*
	 Assertion
	 
	 difference between assert and verify or hard assert and soft assert
	 hard assert:
	 -it allow us to verify result of operation
	 - if this assert failed then it abort that perticular test excution 
	 -it can not allow to next test step execution
	 -then it continue with next test case excution 
	 
	 soft assert:
	 -it allow us to verify result of operation 
	 -if this assert failed then it can not abort the test case execution ..it proceed for next test step 
	 -it catch or collect all the exception and with the help of assertall()method we can see these exception
	 
	 -
	 
	 */

}
